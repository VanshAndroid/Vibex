public class HomeContent {

    private String blog_content_tag;

    private String blog_content_subject;

    private String blog_content_pic;

    private String blog_content;


    public String getBlog_content_tag() {
        return blog_content_tag;
    }

    public void setBlog_content_tag(String blog_content_tag) {
        this.blog_content_tag = blog_content_tag;
    }

    public String getBlog_content_subject() {
        return blog_content_subject;
    }

    public void setBlog_content_subject(String blog_content_subject) {
        this.blog_content_subject = blog_content_subject;
    }

    public String getBlog_content_pic() {
        return blog_content_pic;
    }

    public void setBlog_content_pic(String blog_content_pic) {
        this.blog_content_pic = blog_content_pic;
    }

    public String getBlog_content() {
        return blog_content;
    }

    public void setBlog_content(String blog_content) {
        this.blog_content = blog_content;
    }
}
